// MBTI 타입 결정 로직
export function calculateMBTIResult(answers) {
  const scores = {
    E: 0, I: 0,  // 외향성/내향성
    S: 0, N: 0,  // 감각/직관
    T: 0, F: 0,  // 사고/감정
    J: 0, P: 0   // 판단/인식
  };

  // 답변 점수 계산
  answers.forEach(answer => {
    if (scores.hasOwnProperty(answer)) {
      scores[answer]++;
    }
  });

  // MBTI 타입 결정
  const mbtiType = 
    (scores.E >= scores.I ? 'E' : 'I') +
    (scores.S >= scores.N ? 'S' : 'N') +
    (scores.T >= scores.F ? 'T' : 'F') +
    (scores.J >= scores.P ? 'J' : 'P');

  return mbtiType;
}
