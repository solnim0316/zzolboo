export const calculateDramaResult = (answers) => {
  const scores = {
    잔잔로맨스: 0,
    치정로맨스: 0,
    힐링성장: 0,
    첫사랑청춘: 0,
    무심다정: 0,
    밀당고수: 0,
    개막장: 0,
    양다리: 0,
    얀데레: 0,
    망상: 0
  };

  // 각 답변에 따른 점수 계산
  answers.forEach((answer, index) => {
    const question = dramaTestData.questions[index];
    const selectedOption = question.options[answer];
    
    Object.entries(selectedOption.scores).forEach(([type, score]) => {
      scores[type] += score;
    });
  });

  // 가장 높은 점수를 가진 타입 찾기
  let maxScore = 0;
  let resultKey = '잔잔로맨스';

  Object.entries(scores).forEach(([type, score]) => {
    if (score > maxScore) {
      maxScore = score;
      resultKey = type;
    }
  });

  return resultKey;
}; 